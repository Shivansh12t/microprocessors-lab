; CHECK IF THE ARRAY IS PALINDROME

DATA:
MOV [1000H], 03H;
MOV [1001H], 04H;
MOV [1002H], 05H;
MOV [1003H], 04H;
MOV [1004H], 03H;


START:
MOV BX, 1000H; BASE - REGISTER
MOV SI, 00H; STARTING
MOV DI, 04H; ARRAY LENGTH;
MOV DX, 01H; TO STORE SOLUTION - INITIALLY TRUE

STARTLOOP:
MOV AL, [BX + SI];
CMP AL, [BX + DI];

JE P1;
MOV DX, 00H;
JMP END;
P1:

INC SI;
DEC DI;
CMP SI, DI;
JB STARTLOOP:

END:
HLT;