; LARGEST NUMBER FROM THE LIST

DATA:
MOV [1000H], 0xAA;
MOV [1001H], 0x10;
MOV [1002H], 0x12;
MOV [1003H], 0xFF;
MOV [1004H], 0x9F;

START:
MOV SI, 1000H;
MOV BL, 00H  ; STORE THE LARGEST NUMBER
MOV CL, 05H  ; COUNTER

; PRE LOOP ;
MOV BL, [SI];
INC SI;
DEC CL;

STARTLOOP:
MOV AL, [SI];
CMP AL, BL;
JC NOTBIG;
JZ NOTBIG;
MOV BL, AL;
NOTBIG:
DEC CL;
INC SI;
MOV AL, CL;
OR AL, 00H; JUST FOR SETTING THE FLAGS
JNZ STARTLOOP;

; SOLUTION ; LOOK AT BX
HLT ;