; SERIES OF NUMBER, CHECK IF EVEN THEN ADD

; DATA
; NUMBERS FROM 9000-900A

; CODE
LXI 9000H; ARRAY ADDR
MVI C, 10H; LOOP COUNTER
MVI B, 00H; TO STORE SOL

LOOP: NOP;
; CHECKING EVEN BY ANDING
MOV D, M;
MVI A, 01H;
ANA D;
JNZ NOTZERO;

; IF EVEN ADDING
MOV A, B;
ADD D;
MOV B, A;

; LOOPING
NOTZERO: NOP;
INX H;
DCR C;
MOV A, C;
JNZ LOOP;

HLT;